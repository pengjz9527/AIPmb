"""
从招商银行和汉口银行采集的个人金融产品信息生成Excel文件
"""

import os
from openpyxl import Workbook
from openpyxl.styles import Font, Alignment, Border, Side, PatternFill
from openpyxl.utils import get_column_letter

DATA_DIR = '/Users/pengjizhou/Documents/AIPmb/data'

# 通用样式
HEADER_FONT = Font(name='微软雅黑', bold=True, size=10, color='FFFFFF')
HEADER_FILL = PatternFill(start_color='C41230', end_color='C41230', fill_type='solid')  # 招行红
HEADER_ALIGN = Alignment(horizontal='center', vertical='center', wrap_text=True)
DATA_FONT = Font(name='微软雅黑', size=9)
DATA_ALIGN = Alignment(horizontal='left', vertical='top', wrap_text=True)
CENTER_ALIGN = Alignment(horizontal='center', vertical='center', wrap_text=True)
THIN_BORDER = Border(
    left=Side(style='thin'), right=Side(style='thin'),
    top=Side(style='thin'), bottom=Side(style='thin')
)

# 汉口银行行标识颜色（浅蓝色底）
HKB_FILL = PatternFill(start_color='E8F4FD', end_color='E8F4FD', fill_type='solid')


def create_excel(filename, title, headers, rows, col_widths):
    """创建Excel文件的通用函数"""
    wb = Workbook()
    ws = wb.active
    ws.title = title

    # 找到"银行"列的索引
    bank_col_idx = None
    for i, h in enumerate(headers):
        if h == '银行':
            bank_col_idx = i + 1  # openpyxl列号从1开始
            break

    # 写入表头
    for col, header in enumerate(headers, 1):
        cell = ws.cell(row=1, column=col, value=header)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.alignment = HEADER_ALIGN
        cell.border = THIN_BORDER

    # 写入数据
    for row_idx, row_data in enumerate(rows, 2):
        is_hkb = bank_col_idx and row_data[bank_col_idx - 1] == '汉口银行'
        for col_idx, value in enumerate(row_data, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=value)
            cell.font = DATA_FONT
            cell.alignment = DATA_ALIGN
            cell.border = THIN_BORDER
            # 汉口银行行用浅蓝色背景
            if is_hkb:
                cell.fill = HKB_FILL

    # 设置列宽
    for i, width in enumerate(col_widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = width

    # 冻结首行
    ws.freeze_panes = 'A2'
    ws.auto_filter.ref = f"A1:{get_column_letter(len(headers))}{len(rows)+1}"

    filepath = os.path.join(DATA_DIR, filename)
    wb.save(filepath)
    print(f"已生成: {filepath} ({len(rows)} 条记录)")


